MatrixXd set_focus(double f) {

    std::ifstream file("image.pgm");

    PGMObject p;
    file >> p;

    MatrixXd M;
    p.get_data(M);

    ep = std::max(std::abs(f - f0),
                  std::numeric_limits<double>::epsilon);

    unsigned int s = 16;
    MatrixXd S(s,s);
    for(unsigned int i = 0; i < s; ++i) {
        for(unsigned int j = 0; j < s; ++j) {
            S(i,j) = 1. / (1 + (i*i + j*j)/ep);
        }
    }

    S /= S.sum();
}
